package ex03;
public class Ex03 {
	public static void main(String[] args) {
		// 실습 준비 완료
		int x = 10;
		double y = 1.5;
		float f1 = 21.34f;
		int a = 1, b = 2, c = 3;
		boolean t = true;
		// \n 줄바꿈 \t 4칸 띄어쓰기 
		System.out.printf("정수 x : %d\n", x);
		System.out.printf("실수 y : %f\n", y);
		System.out.printf("실수 f1 : %f\n", f1);
		System.out.printf("정수 a, b, c : %d, %d, %d\n", a, b, c);
		System.out.printf("논리형 t : %b\n", t);
		
	}
}
